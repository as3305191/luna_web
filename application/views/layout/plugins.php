var ms = $('.marquee').find('div');
var cnt = 0;


function showMarquee() {
	if(cnt > 0) {
		$(ms[cnt - 1]).animate({'top': '-30px'},500).animate({'top': '30px'},0);
	}
	$(ms[cnt++]).animate({'top': '0px'},500);
	setTimeout(function(){
		cnt = (cnt % ms.length);
		if(cnt == 0) {
			$(ms[ms.length - 1]).animate({'top': '-30px'},500).animate({'top': '30px'},0);
		}
		showMarquee();
	}, 4000);
}
showMarquee();
</script>
